package trabalho.univas.vo;

public class Farmaceutico {
	
	private String nameFarmaceutico;
	private String crf;
	private String uf;
	private int id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNameFarmaceutico() {
		return nameFarmaceutico;
	}
	public void setNameFarmaceutico(String nameFarmaceutico) {
		this.nameFarmaceutico = nameFarmaceutico;
	}
	public String getCrf() {
		return crf;
	}
	public void setCrf(String crf) {
		this.crf = crf;
	}
	public String getUf() {
		return uf;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
	
	
}
