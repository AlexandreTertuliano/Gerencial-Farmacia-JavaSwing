package trabalho.univas.vo;

public class Farmacia {
	private String nameFarmacia;
	private String cnpj;
	private String cidade;
	private String rua;
	private String numero;
	private int id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNameFarmacia() {
		return nameFarmacia;
	}
	public void setNameFarmacia(String nameFarmacia) {
		this.nameFarmacia = nameFarmacia;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	
	
}
